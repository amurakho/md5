#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	c;
	char	nbr;

	c = '-';
	if (n == -2147483648)
	{
		write(fd, "-2147483648", ft_strlen("-2147483648"));
		return ;
	}
	if (n > -2147483648)
	{
		if (n < 0)
		{
			n *= -1;
			write(fd, &c, 1);
		}
		if (n >= 10)
			ft_putnbr_fd((n / 10), fd);
		nbr = (n % 10) + '0';
	}
	write(fd, &nbr, 1);
}
