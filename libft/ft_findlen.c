int	ft_findlen(int n)
{
	int		len;
	int		counter;

	len = 0;
	counter = 1;
	if (n < 999999999)
	{
		while (n >= counter)
		{
			counter *= 10;
			len++;
		}
	}
	else
		len = 10;
	if (n == 0)
		len = 1;
	return (len);
}
